package org.patternprogram;
import java.util.Scanner;
public class Pattern1 {
	public static void main(String[] arg) {
		System.out.println("Enter no of times patterns to be printed ");
		
	Scanner sc= new Scanner(System.in);
	int n=sc.nextInt();
	for(int i=1;i<=n;i++)
	{
		System.out.println();
		for(int j=1;j<=i;j++)
		{
			System.out.print(j);
		}
		
		}
			
		}
	}
	
	


